// processBlockWithSequencerLogic processes a single block using sequencer logic and returns the new state root
func processBlockWithSequencerLogic(logger log.Logger, config *params.ChainConfig, chCtx *remoteChainCtx, replayMiner *ReplayMiner, block *types.Block,
	outW io.Writer, outDir string) (*core.ProcessResult, common.Hash, error) {

	header := block.Header()

	vmCfg := vm.Config{Tracer: nil}

	// Set up tracing only if output is requested
	if outW != nil && outW != io.Discard {
		vmCfg.Tracer = logger2.NewJSONLogger(&logger2.Config{
			EnableMemory:     false,
			DisableStack:     false,
			DisableStorage:   false,
			EnableReturnData: false,
			Limit:            0,
			Overrides:        nil,
		}, outW)
	}

	// Witness creation and prefetcher management is now handled in makeEnv method
	// to align with miner logic and avoid duplication

	params := extractGenerateParams(block)

	// Use miner's call chain: generateWork -> prepareWork -> makeEnv -> commitTransaction -> applyTransaction
	// Pass vmCfg to enable tracing
	result := replayMiner.generateWork(params, outDir != "", block, vmCfg)
	if result.err != nil {
		return nil, common.Hash{}, fmt.Errorf("failed to generate work: %w", result.err)
	}

	// Export witness data for debugging if output directory is specified (replay-specific logic)
	// This is now handled after generateWork to align with miner logic structure
	if outDir != "" && result.witness != nil {
		witnessDump := result.witness.ToExecutionWitness()
		out, err := json.MarshalIndent(witnessDump, "", "  ")
		if err != nil {
			logger.Error("failed to encode witness", "err", err)
		} else {
			witnessPath := fmt.Sprintf("%s/block_%d_witness.json", outDir, block.NumberU64())
			if err := os.WriteFile(witnessPath, out, 0644); err != nil {
				logger.Debug("Failed to write witness", "err", err, "path", witnessPath)
			} else {
				logger.Info("Witness data exported", "path", witnessPath)
			}
		}
	}

	// Commit the state changes and get the new state root
	newStateRoot, err := chCtx.currentState.Commit(uint64(block.NumberU64()), config.IsEIP158(header.Number), config.IsCancun(header.Number, header.Time))
	if err != nil {
		return nil, common.Hash{}, fmt.Errorf("failed to commit state: %w", err)
	}

	return &core.ProcessResult{
		Receipts: result.receipts,
		Requests: result.requests,
		Logs: func() []*types.Log {
			var allLogs []*types.Log
			for _, r := range result.receipts {
				allLogs = append(allLogs, r.Logs...)
			}
			return allLogs
		}(),
		GasUsed: result.block.GasUsed(),
	}, newStateRoot, nil
}

