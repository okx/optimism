// NewReplayMiner creates a new replay miner instance
func NewReplayMiner(config *params.ChainConfig, chain *remoteChainCtx, logger log.Logger) *ReplayMiner {
	return &ReplayMiner{
		chainConfig: config,
		chain:       chain,
		logger:      logger,
	}
}

// extractGenerateParams extracts miner parameters from an existing block
// This is replay-specific logic that miner doesn't have
func extractGenerateParams(block *types.Block) *generateParams {
	return &generateParams{
		timestamp:     block.Time(),
		forceTime:     true, // Timestamp is fixed in replay mode
		parentHash:    block.ParentHash(),
		coinbase:      block.Coinbase(),
		random:        block.MixDigest(),
		withdrawals:   block.Withdrawals(),
		beaconRoot:    block.BeaconRoot(),
		noTxs:         true, // Transactions are forced-included in replay mode
		txs:           block.Transactions(),
		gasLimit:      func() *uint64 { limit := block.GasLimit(); return &limit }(),
		eip1559Params: nil,
		interrupt:     nil,
		isUpdate:      false,
		rpcCtx:        nil,
	}
}

// generateWork mimics miner's generateWork method
// DIFFERENCES FROM MINER:
// - Skips fillTransactions logic (transactions are already determined)
// - Skips interrupt checks (no interruption needed in replay)
// - Uses existing block for FinalizeAndAssemble
// - Receives vmCfg for tracing support
func (rm *ReplayMiner) generateWork(params *generateParams, witness bool, block *types.Block, vmCfg vm.Config) *newPayloadResult {
	work, err := rm.prepareWork(params, witness, block, vmCfg)
	if err != nil {
		return &newPayloadResult{err: err}
	}

	if work.gasPool == nil {
		gasLimit := work.header.GasLimit
		work.gasPool = new(core.GasPool).AddGas(gasLimit)
	}

	misc.EnsureCreate2Deployer(rm.chainConfig, work.header.Time, work.state)

	// Process forced transactions (from block)
	for i, tx := range params.txs {
		rm.logger.Info("Processing tx", "i", i, "hash", tx.Hash().Hex())
		from, _ := types.Sender(work.signer, tx)
		work.state.SetTxContext(tx.Hash(), work.tcount)
		err = rm.commitTransaction(work, tx)
		if err != nil {
			return &newPayloadResult{err: fmt.Errorf("failed to force-include tx: %s type: %d sender: %s nonce: %d, err: %w", tx.Hash(), tx.Type(), from, tx.Nonce(), err)}
		}
	}

	// SKIPPED: fillTransactions logic (not needed in replay mode)
	// SKIPPED: interrupt checks (no interruption needed in replay mode)

	body := types.Body{Transactions: work.txs, Withdrawals: params.withdrawals}
	allLogs := make([]*types.Log, 0)
	for _, r := range work.receipts {
		allLogs = append(allLogs, r.Logs...)
	}

	isIsthmus := rm.chainConfig.IsIsthmus(work.header.Time)

	// Collect consensus-layer requests if Prague is enabled
	var requests [][]byte
	if rm.chainConfig.IsPrague(work.header.Number, work.header.Time) && !isIsthmus {
		requests = [][]byte{}
		// EIP-6110 deposits
		if err := core.ParseDepositLogs(&requests, allLogs, rm.chainConfig); err != nil {
			return &newPayloadResult{err: err}
		}
		// EIP-7002
		if err := core.ProcessWithdrawalQueue(&requests, work.evm); err != nil {
			return &newPayloadResult{err: err}
		}
		// EIP-7251 consolidations
		if err := core.ProcessConsolidationQueue(&requests, work.evm); err != nil {
			return &newPayloadResult{err: err}
		}
	}

	if isIsthmus {
		requests = [][]byte{}
	}

	if requests != nil {
		reqHash := types.CalcRequestsHash(requests)
		work.header.RequestsHash = &reqHash
	}

	// Use FinalizeAndAssemble to match miner logic exactly
	newBlock, err := rm.chain.Engine().FinalizeAndAssemble(rm.chain, work.header, work.state, &body, work.receipts)
	if err != nil {
		return &newPayloadResult{err: err}
	}

	return &newPayloadResult{
		block:    newBlock,
		fees:     totalFees(newBlock, work.receipts),
		sidecars: work.sidecars,
		stateDB:  work.state,
		receipts: work.receipts,
		requests: requests,
		witness:  work.witness,
	}
}

// prepareWork mimics miner's prepareWork method
// DIFFERENCES FROM MINER:
// - Uses external parent block instead of fetching from chain
// - Uses existing block's Extra and BaseFee instead of computing them
// - Simplified gas limit handling
// - Receives vmCfg for tracing support
func (rm *ReplayMiner) prepareWork(genParams *generateParams, witness bool, block *types.Block, vmCfg vm.Config) (*environment, error) {
	// In replay mode, we don't need to check parent block since we already have the complete block
	parent := rm.chain.CurrentHeader()

	// Timestamp validation
	timestamp := genParams.timestamp
	if parent.Time >= timestamp {
		if genParams.forceTime {
			return nil, fmt.Errorf("invalid timestamp, parent %d given %d", parent.Time, timestamp)
		}
		timestamp = parent.Time + 1
	}

	// Construct block header
	header := &types.Header{
		ParentHash: parent.Hash(),
		Number:     new(big.Int).Add(parent.Number, common.Big1),
		GasLimit:   parent.GasLimit, // Use parent's gas limit instead of computing
		Time:       timestamp,
		Coinbase:   genParams.coinbase,
	}

	// Use existing block's Extra data instead of computing
	header.Extra = block.Extra()

	if genParams.random != (common.Hash{}) {
		header.MixDigest = genParams.random
	}

	// Set EIP-1559 related fields
	if rm.chainConfig.IsLondon(header.Number) {
		// Use existing block's BaseFee instead of computing
		header.BaseFee = block.BaseFee()
	}

	// Run consensus preparation
	if err := rm.chain.Engine().Prepare(rm.chain, header); err != nil {
		rm.logger.Error("Failed to prepare header for sealing", "err", err)
		return nil, err
	}

	// Apply EIP-4844, EIP-4788
	if rm.chainConfig.IsCancun(header.Number, header.Time) {
		var excessBlobGas uint64
		if rm.chainConfig.IsCancun(parent.Number, parent.Time) {
			excessBlobGas = eip4844.CalcExcessBlobGas(rm.chainConfig, parent, timestamp)
		}
		header.BlobGasUsed = new(uint64)
		header.ExcessBlobGas = &excessBlobGas
		header.ParentBeaconRoot = genParams.beaconRoot
	}

	// Create environment
	env, err := rm.makeEnv(header, genParams.coinbase, witness, genParams.rpcCtx, vmCfg)
	if err != nil {
		rm.logger.Error("Failed to create sealing context", "err", err)
		return nil, err
	}

	env.noTxs = genParams.noTxs

	// Process beacon root
	if header.ParentBeaconRoot != nil {
		core.ProcessBeaconBlockRoot(*header.ParentBeaconRoot, env.evm)
	}

	// Process Prague related logic
	if rm.chainConfig.IsPrague(header.Number, header.Time) {
		core.ProcessParentBlockHash(header.ParentHash, env.evm)
	}

	return env, nil
}

// makeEnv mimics miner's makeEnv method
// DIFFERENCES FROM MINER:
// - Uses external state instead of fetching from chain
// - Simplified state access logic
// - Handles witness export for replay debugging
// - Receives vmCfg for tracing support
func (rm *ReplayMiner) makeEnv(header *types.Header, coinbase common.Address, witness bool, rpcCtx context.Context, vmCfg vm.Config) (*environment, error) {
	// In replay mode, we use externally provided state
	state := rm.chain.currentState

	if witness {
		bundle, err := stateless.NewWitness(header, rm.chain)
		if err != nil {
			return nil, err
		}
		state.StartPrefetcher("replay", bundle)

		// Export witness data for debugging (replay-specific logic)
		// This is moved from processBlockWithSequencerLogic to align with miner logic
		if rm.chain.logger != nil {
			rm.chain.logger.Info("Witness created and prefetcher started for replay debugging")
		}
	}

	return &environment{
		signer:   types.MakeSigner(rm.chainConfig, header.Number, header.Time),
		state:    state,
		coinbase: coinbase,
		header:   header,
		witness:  state.Witness(),
		evm:      vm.NewEVM(core.NewEVMBlockContext(header, rm.chain, &coinbase, rm.chainConfig, state), state, rm.chainConfig, vmCfg),
		rpcCtx:   rpcCtx,
	}, nil
}

// commitTransaction mimics miner's commitTransaction method
// DIFFERENCES FROM MINER:
// - Skips interop transaction checks (not needed in replay mode)
// - Simplified conditional transaction handling
func (rm *ReplayMiner) commitTransaction(env *environment, tx *types.Transaction) error {
	// Check conditional transaction if present
	if conditional := tx.Conditional(); conditional != nil {
		// Check header conditional
		if err := env.header.CheckTransactionConditional(conditional); err != nil {
			return fmt.Errorf("failed header check: %s: %w", err, errTxConditionalInvalid)
		}
		// Check state conditional
		if err := env.state.CheckTransactionConditional(conditional); err != nil {
			return fmt.Errorf("failed state check: %s: %w", err, errTxConditionalInvalid)
		}
	}

	receipt, err := rm.applyTransaction(env, tx)
	if err != nil {
		return err
	}
	env.txs = append(env.txs, tx)
	env.receipts = append(env.receipts, receipt)
	env.tcount++
	return nil
}

// applyTransaction mimics miner's applyTransaction method
// DIFFERENCES FROM MINER:
// - None (completely identical logic)
func (rm *ReplayMiner) applyTransaction(env *environment, tx *types.Transaction) (*types.Receipt, error) {
	var (
		snap = env.state.Snapshot()
		gp   = env.gasPool.Gas()
	)
	receipt, err := core.ApplyTransaction(env.evm, env.gasPool, env.state, env.header, tx, &env.header.GasUsed)
	if err != nil {
		env.state.RevertToSnapshot(snap)
		env.gasPool.SetGas(gp)
	}
	return receipt, err
}

